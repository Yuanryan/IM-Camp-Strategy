async function main() {
  await prisma.question.deleteMany(); // 只清題庫，重新載入
  await prisma.question.createMany({
    data: rows.map((r) => ({
      gameName: r.gameName,
      prompt: r.prompt,
      answer: r.answer ?? null,
      difficulty: r.difficulty ?? null,
    })),
  });
  const byGame = rows.reduce<Record<string, number>>((m, r) => ((m[r.gameName] = (m[r.gameName] ?? 0) + 1), m), {});
  console.log(`✅ 已載入 ${rows.length} 題`);
  for (const [g, n] of Object.entries(byGame)) console.log(`  ${g}：${n} 題`);
}

main()
